public class ForMultiploOuNaoDe3 {
    public static void main(String[] args) {
        for (int i = 1; i < 100; i++){
            if (i % 3 == 0){
                System.out.println(i+" é multiplo de 3");
            }else{
                System.out.println(i+" não é multiplo de 3");
            }
        } 
    }
}
